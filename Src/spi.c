/**
  * @name         SPI_SendByte
	* @brief        SPI2发送一个字节的数据
	* @param        TxData:需用SPI2发送出去的一个字节的数据
  * @retval       SPI2返回的一个字节的数据
	* @lastModify   2018/9/25  17:50
	* @author       JackWilliam
  */
uint8_t SPI_SendByte(uint8_t TxData)
{
	uint8_t Rxdata;
	HAL_SPI_TransmitReceive(&hspi2,&TxData,&Rxdata,1, 1000);
//	HAL_SPI_Transmit(&hspi2, &TxData, 1, 1000);
	return Rxdata;
}

