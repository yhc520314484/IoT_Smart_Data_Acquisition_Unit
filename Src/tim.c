/**
  * @name         HAL_TIM_PeriodElapsedCallback
  * @brief        定时器回调函数
  * @param        htim:指向所使用的定时器的指针
  * @retval       none
	* @lastModify   2018/9/25  22:24
	* @author       JackWilliam
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	static uint64_t watchdog_count = 0;              //看门狗计数器
	
	if (htim->Instance == htim3.Instance)
  {
			watchdog_count++;
			if(watchdog_count == WatchDogFeedPeriod){
				watchdog_count = 0;
				HAL_IWDG_Refresh(&hiwdg);                  //4S进行喂狗
			}
	}
}
